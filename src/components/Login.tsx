import React, { useState } from 'react';
import axios from 'axios';
import '../styles/Login.css'; // Optional, für Styling

const Login: React.FC = () => {
  const [username, setUsername] = useState('');
  const [password, setPassword] = useState('');
  const [error, setError] = useState('');

  const handleLogin = async (e: React.FormEvent) => {
    e.preventDefault();
    try {
      const response = await axios.post('http://localhost:4200/login', {
        username,
        password,
      });
      // Annahme: das Token wird im response.data.token zurückgegeben
      localStorage.setItem('token', response.data.token);
      // Weiterleitung oder andere Aktionen nach erfolgreichem Login
    } catch (err) {
      setError('Anmeldedaten sind ungültig');
    }
  };

  return (
    <div className="login-container">
      <h2>Login</h2>
      {error && <p className="error">{error}</p>}
      <form onSubmit={handleLogin}>
        <div>
          <label htmlFor="username">Username</label>
          <input
            type="text"
            id="username"
            value={username}
            onChange={(e) => setUsername(e.target.value)}
            required
          />
        </div>
        <div>
          <label htmlFor="password">Password</label>
          <input
            type="password"
            id="password"
            value={password}
            onChange={(e) => setPassword(e.target.value)}
            required
          />
        </div>
        <button type="submit">Login</button>
      </form>
    </div>
  );
};

export default Login;
